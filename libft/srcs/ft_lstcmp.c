#include "libft.h"

int	ft_lstcmp(t_list *lst1, t_list *lst2)
{
	while (lst1 || lst2)
	{
		if (lst1 != lst2)
			return (0);
		lst1 = lst1->next;
		lst2 = lst2->next;
	}
	return (1);
}
